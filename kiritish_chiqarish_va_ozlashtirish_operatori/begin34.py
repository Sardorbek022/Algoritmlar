# Shokolad kilogramni kiritib olamiz
x = int(input(" Shokolad kgni kiriting = "))
# Shokolad hammasi narxini kiritib olamiz
a = int(input(" Shokolad umumiy narxini kiriting = "))
# Konfet kilogramni kiritib olamiz
y = int(input(" Konfet kgni kiriting = "))
# Shokolad hammasi narxini kiritib olamiz
b = int(input(" Konfet umumiy narxini kiriting = "))
# shokoladning konfetdan qancha qimmatligi hisoblaymiz
farq_som = ( a / x ) - ( b / y )
# shokoladning konfetdan qancha qimmatligi ekranga chiqarib olamiz
print(" Farq narxi = ",farq_som)