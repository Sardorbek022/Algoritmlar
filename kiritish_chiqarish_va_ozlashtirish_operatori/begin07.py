# Aylananing radusi kiritib olamiz
radius = int(input(" Aylananing radiusini kiriting = "))
# O'zgarmasni kiritamiz
PI = 3.14
# Aylananing uzunligini hisoblaymiz
L = 2 * PI * radius
# Aylananing yuzasini hisoblaymiz
S = PI * radius * radius
# Aylananing uzunligi natijasini ekranga chiqaramiz
print(" Aylananing uzunligi = ",L)
# Aylananing yuzasi natijasini ekranga chiqaramiz
print(" Aylananing yuzasi = ",S)