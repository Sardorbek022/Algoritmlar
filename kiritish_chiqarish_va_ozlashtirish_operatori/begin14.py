#   Aylananing uzunligi  kiritib olyapmiz
aylana_uzunligi = int(input(" Aylananing uzunligi kiriting = "))
# O'zgarmasni kiritamiz
PI = 3.14
# Aylananing radiusini hisoblaymiz
radius = aylana_uzunligi / ( 2 * PI)
# Aylananing yuzasini hisoblaymiz
aylana_yuzasi = PI * radius ** 2
# Aylananing radiusini ekranga chiqaramiz
print(" Aylananing radiusi = ",radius)
#  Aylananing yuzasini ekranga chiqaramiz
print(" Aylananing yuzasi = ",aylana_yuzasi)

