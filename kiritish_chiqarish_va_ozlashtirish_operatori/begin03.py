#  a ni kiritib olyapmiz
a = int(input(" To'rtburchakning a tomonini kiriting = "))
#  b ni kiritib olyapmiz
b = int(input(" To'rtburchakning b tomonini kiriting = "))
# To'rtburchak yuzasini hisoblayapmiz
