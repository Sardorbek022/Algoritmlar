#  a ni kiritib olyapmiz
a = int(input(" a sonni kiriting = "))
#  b ni kiritib olyapmiz
b = int(input(" b sonni kiriting = "))
# Ikki sonning yig'indisi
yigindi = a + b
# Ikki sonning ko'paytmasi
kopaytma = a * b
#  a sonning kvadrati darajaga oshirish uchun ikki yulduzcha usulidan foydalanamiz
a_kvadrati = a ** 2
#  b sonning kvadrati darajaga oshirish uchun ikki yulduzcha usulidan foydalanamiz
b_kvadrati = b ** 2
# Ikki sonning yig'indisi natijasini ekranga chiqaramiz
print(" Ikki sonning yig'indisi = ",yigindi)
# Ikki sonning ko'oaytmasi natijasini ekranga chiqaramiz
print(" Ikki sonnig ko'paytmasi = ",kopaytma)
# a sonning kvarati natiiasi ekranga chiqaramiz
print(" a sonning kvadrati = ", a_kvadrati)
# b sonning kvarati natiiasi ekranga chiqaramiz
print(" b sonning kvadrati = ", b_kvadrati)